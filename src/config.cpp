static void cleanUpPaths(QStringList &str)
{
   for (auto &sfp : str) {  

      if (sfp.isEmpty()) {
         continue;
      }

      sfp.replace("\\", "/");
    
      QByteArray path = sfp.toUtf8();

      if ((path.at(0) != '/' && (path.length() <= 2 || path.at(1) != ':')) || path.at(path.length() - 1) != '/' ) {

         QFileInfo fi(path);

         if (fi.exists() && fi.isDir()) {
            sfp = fi.absoluteFilePath() + "/";           
         }
      }
      
   }
}

