static QByteArray configFileToString(const char *name)
{
   if (name == 0 || name[0] == 0) {
      return 0;
   }
   QFile f;

   bool fileOpened = FALSE;

   if (name[0] == '-' && name[1] == 0) { // read from stdin
      fileOpened = f.open(stdin, QIODevice::ReadOnly);

      if (fileOpened) {
         const int bSize = 4096;

         QByteArray contents;
         contents.resize(bSize);

         int totalSize = 0;
         int size;

         while ((size = f.read(contents.data() + totalSize, bSize)) == bSize) {
            totalSize += bSize;
            contents.resize(totalSize + bSize);
         }

         totalSize += size + 1;
         contents.resize(totalSize);

         // to help the scanner   
         contents[totalSize - 1] = '\n'; 

         return contents;
      }

   } else { // read from file
      QFileInfo fi(name);

      if (!fi.exists() || !fi.isFile()) {
         config_err("Error: file `%s' not found\n", name);
         return "";
      }

      f.setFileName(name);
      fileOpened = f.open(QIODevice::ReadOnly);

      if (fileOpened) {
         int fsize = f.size();

         QByteArray contents;
         contents.resize(fsize + 2);

         f.read(contents.data(), fsize);
         f.close();

         if (fsize == 0 || contents[fsize - 1] == '\n') {
            contents[fsize] = '\0';
         } else {
            contents[fsize] = '\n';   // to help the scanner
         }
         contents[fsize + 1] = '\0';
         return contents;
      }
   }
   if (!fileOpened) {
      config_err("Error: cannot open file `%s' for reading\n", name);
   }
   return "";
}

bool Config::parseString(const char *fn, const char *str, bool update)
{
   config = Config::instance();
   inputString   = str;
   inputPosition = 0;
   yyFileName    = fn;
   yyLineNr      = 1;
 
   includeStack.clear();
   includeDepth  = 0;

   configYYrestart( configYYin );

   BEGIN( Start );
   config_upd = update;
   configYYlex();
   config_upd = FALSE;
   inputString = 0;
   return TRUE;
}

bool Config::parse(const char *fn, bool update)
{
   int retval;
   encoding = "UTF-8";
   printlex(configYY_flex_debug, TRUE, __FILE__, fn);
   retval =  parseString(fn, configFileToString(fn), update);
   printlex(configYY_flex_debug, FALSE, __FILE__, fn);
   return retval;
}

