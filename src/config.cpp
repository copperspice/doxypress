static int yyread(char *buf, int max_size)
{
   // no file included
   if (includeStack.isEmpty()) {
      int c = 0;

      if (inputString == 0) {
         return c;
      }

      while ( c < max_size && inputString[inputPosition] ) {
         *buf = inputString[inputPosition++] ;
         c++;
         buf++;
      }

      return c;

   } else {      

      FILE *temp = includeStack.top()->filePtr;
      return (int)fread(buf, 1, max_size, temp);

   }
}

static QByteArray configStringRecode(const QByteArray &str, const char *fromEncoding, const char *toEncoding)
{
   QByteArray inputEncoding  = fromEncoding;
   QByteArray outputEncoding = toEncoding;

   if (inputEncoding.isEmpty() || outputEncoding.isEmpty() || inputEncoding == outputEncoding) {
      return str;
   }

   QTextCodec *inCodec  = QTextCodec::codecForName(inputEncoding);
   QTextCodec *outCodec = QTextCodec::codecForName(outputEncoding);
  
   if (! inCodec || ! outCodec) {
      err("Unsupported character conversion: '%s'->'%s': %s\n"
          "Check the INPUT_ENCODING setting in the config file!\n",
          qPrintable(inputEncoding), qPrintable(outputEncoding), strerror(errno));

      exit(1);
   }

   QString temp = inCodec->toUnicode(str);
   QByteArray output = outCodec->fromUnicode(temp);
             
   return output;
}

