static QByteArray convertToComment(const QByteArray &s, const QByteArray &u)
{
   //printf("convertToComment(%s)=%s\n",s.data(),u.data());
   QByteArray result;
   if (!s.isEmpty()) {
      QByteArray tmp = s.trimmed();
      char *p = tmp.data();
      char c;
      result += "#";
      if (*p && *p != '\n') {
         result += " ";
      }
      while ((c = *p++)) {
         if (c == '\n') {
            result += "\n#";
            if (*p && *p != '\n') {
               result += " ";
            }
         } else {
            result += c;
         }
      }
      result += '\n';
   }
   if (!u.isEmpty()) {
      if (!result.isEmpty()) {
         result += '\n';
      }
      result += u;
   }
   return result;
}

