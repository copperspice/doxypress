static void substEnvVarsInString(QByteArray &s)
{
   // For e.g. PROGRAMFILES(X86)

   static QRegExp re("\\$\\([a-z_A-Z0-9.-]+\\)");
   static QRegExp re2("\\$\\([a-z_A-Z0-9.-]+\\([a-z_A-Z0-9.-]+\\)\\)"); 

   if (s.isEmpty()) {
      return;
   }

   int p = 0;
   int i, l;
  
   while ((i = re.indexIn(s, p)) != -1 || (i = re2.indexIn(s, p)) != -1) {      
      l = re2.matchedLength();

      QByteArray env = portable_getenv(s.mid(i + 2, l - 3));
      substEnvVarsInString(env); // recursively expand variables if needed.

      s = s.left(i) + env + s.right(s.length() - i - l);
      p = i + env.length(); // next time start at the end of the expanded string
   }

   // to strip the bogus space that was added when an argument
   s = s.trimmed();  
}

static void substEnvVarsInStrList(QStringList &sl)
{
   for (int index = 0; index < sl.count(); ++index) {

      QString s = sl[index];
      QByteArray result(s.toUtf8());

      // an argument with quotes will have an extra space at the end, so wasQuoted will be TRUE.
      bool wasQuoted = (result.indexOf(' ') != -1) || (result.indexOf('\t') != -1);

      // here we strip the quote again
      substEnvVarsInString(result);

      if (! wasQuoted) {

         /* as a result of the expansion, a single string may have expanded into a list, which we will add to sl.
            If the original string already contained multiple elements no further splitting is done to allow quoted
            items with spaces! */

         int l = result.length();
         int i, p = 0;

         // skip spaces, search for a "word"

         for (i = 0; i < l; i++) {
            char c = 0;
            // skip until start of new word

            while (i < l && ((c = result.at(i)) == ' ' || c == '\t')) {
               i++;
            }

            p = i; // p marks the start index of the word
            // skip until end of a word

            while (i < l && ((c = result.at(i)) != ' ' && c != '\t' && c != '"')) {
               i++;
            }

            if (i < l) { // not at the end of the string
               if (c == '"') { // word within quotes
                  p = i + 1;

                  for (i++; i < l; i++) {
                     c = result.at(i);

                     if (c == '"') { // end quote
                        // replace the string in the list and go to the next item.

                        // insert new item before current item
                        sl[index] = result.mid(p, i - p); 
                      
                        p = i + 1;
                        break;

                     } else if (c == '\\') { // skip escaped stuff
                        i++;
                     }
                  }

               } else if (c == ' ' || c == '\t') { // separator
                  // replace the string in the list and go to the next item.

                  sl[index] = result.mid(p, i - p); // insert new item before current item.
                
                  p = i + 1;
               }
            }
         }

         if (p != l) { // add the leftover as a string
            // replace the string in the list and go to the next item.
            sl[index] = result.right(l - p);    // insert new item before current item.
          
         }

      } else { // just goto the next element in the list
         sl[index] = result;        

      }    
   }
}

