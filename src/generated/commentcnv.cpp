static int yyread(char *buf, int max_size)
{
   int c = 0;

   while (g_inBuf[g_inBufPos] != 0) {

      QString tmp1    = g_inBuf.at(g_inBufPos);
      QByteArray tmp2 = tmp1.toUtf8();

      if (c + tmp2.length() >= max_size)  {
         // buffer is full
         break;
      }

      c += tmp2.length();

      for (auto letters : tmp2) {
         *buf = letters;
          buf++;
      }

      g_inBufPos++;
   }

   return c;
}
