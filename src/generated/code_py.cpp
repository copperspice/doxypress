static int yyread(char *buf, int max_size)
{
   int c = 0;

   while (g_inputString[g_inputPosition] != 0) {

      QString tmp1    = g_inputString.at(g_inputPosition);
      QByteArray tmp2 = tmp1.toUtf8();

      if (c + tmp2.length() >= max_size)  {
         // buffer is full
         break;
      }

      c += tmp2.length();

      for (auto letters : tmp2) {
         *buf = letters;
          buf++;
      }

      g_inputPosition++;
   }

   return c;
}

